    {
        $user = auth()->user();
        if(!$user) {
            return response(['error' => 'Пользователь не авторизован'], Response::HTTP_UNAUTHORIZED);
        }

        Message::create(['text' => $request->get('text'), 'user_id' => auth()->user()->id]);
    }

